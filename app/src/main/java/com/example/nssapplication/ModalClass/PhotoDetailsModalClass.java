package com.example.nssapplication.ModalClass;

public class PhotoDetailsModalClass {
    String photo;
    String detials;

    public PhotoDetailsModalClass(String photo, String detials) {
        this.photo = photo;
        this.detials = detials;
    }

    public String getPhoto() {
        return photo;
    }

    public String getDetials() {
        return detials;
    }
}
